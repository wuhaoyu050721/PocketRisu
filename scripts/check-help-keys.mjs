#!/usr/bin/env node
/**
 * check-help-keys.mjs
 *
 * Static analyzer for the `language.help` system.
 *
 * What it checks:
 *  1. **Missing keys** — `<Help key="X"/>` or `helpKey: 'X'` references
 *     in source code that have no entry in `src/lang/help.en.ts`.
 *     (These render with an empty modal at runtime.)
 *  2. **Dead keys** — entries defined in `help.en.ts` that no source file
 *     references. Candidates for removal.
 *  3. **Untranslated keys** — keys present in `help.en.ts` but missing in
 *     `help.ko.ts`. The user sees the English text as a `lodash/merge` fallback.
 *
 * Usage:
 *   node scripts/check-help-keys.mjs           # report-only (exit 0)
 *   node scripts/check-help-keys.mjs --strict  # exit 1 if any issue found
 *   node scripts/check-help-keys.mjs --json    # emit structured JSON
 *
 * The script parses help.en.ts / help.ko.ts as plain text — it does NOT
 * import them. This avoids needing a TS toolchain and matches the simplicity
 * of the help system itself.
 *
 * See `.agent/notes/help-audit/` (workspace) and `.agent/guide/ui.md` for context.
 */

import { readFile, readdir } from 'node:fs/promises'
import { join, relative } from 'node:path'
import { fileURLToPath } from 'node:url'

const ROOT = join(fileURLToPath(import.meta.url), '..', '..')

const HELP_EN = 'src/lang/help.en.ts'
const HELP_KO = 'src/lang/help.ko.ts'
const SCAN_DIRS = ['src']
const SCAN_EXTS = ['.ts', '.svelte']
// Files that intentionally define the keys themselves — exclude from
// "reference scan". Otherwise every help.en.ts entry counts as referencing
// itself.
const SELF_FILES = new Set([HELP_EN, HELP_KO])

const args = process.argv.slice(2)
const strict = args.includes('--strict')
const asJson = args.includes('--json')

// ---------------------------------------------------------------------------
// Step 1: parse help.{en,ko}.ts to get the set of defined keys

/**
 * Extract top-level keys from a `helpXx = { ... }` object literal.
 *
 * Why regex (not parser): the file structure is mechanically generated by us
 * (one prop per line or `prop:\n    "value"...`). We only need to find lines
 * matching `<spaces><key>:` at the top level of the object. The regex below
 * matches both `key:` and `"key":` syntax and ignores deeper indentation
 * (which corresponds to nested string-template content).
 *
 * Note: top-level entries always start with exactly 8 spaces in the
 * extracted files (preserved indentation from the original lang/*.ts).
 */
function extractKeys(source) {
    const keys = new Set()
    const re = /^ {8}(?:"([^"]+)"|([A-Za-z_$][A-Za-z0-9_$]*))\s*:/
    for (const line of source.split('\n')) {
        const m = re.exec(line)
        if (!m) continue
        keys.add(m[1] ?? m[2])
    }
    return keys
}

// ---------------------------------------------------------------------------
// Step 2: scan source for help-key references

/**
 * Match patterns:
 *   <Help key="foo"/> / <Help key='foo'/> / <Help key={'foo'}/>
 *   helpKey: 'foo' / helpKey: "foo"
 *   help: 'foo' (Accordion) — only when used together with a `<Help/>`
 *     wouldn't apply; here `help="foo"` for Accordion attribute also triggers.
 *
 * We capture all of these. False positives are tolerable as long as they
 * resolve to an existing key.
 */
const REFERENCE_PATTERNS = [
    // <Help key="foo"/> — Svelte component
    /<Help[^>]*\bkey\s*=\s*['"]([^'"]+)['"]/g,
    // <Help key={'foo'}/> — bound expression literal
    /<Help[^>]*\bkey\s*=\s*\{\s*['"]([^'"]+)['"]\s*\}/g,
    // helpKey: 'foo' or helpKey: "foo" — SettingItem property
    /\bhelpKey\s*:\s*['"]([^'"]+)['"]/g,
    // help="foo" — Accordion `help` attribute (used in OpenrouterSettings)
    /\bhelp\s*=\s*['"]([^'"]+)['"]/g,
]

async function* walkSource(dir) {
    const entries = await readdir(dir, { withFileTypes: true })
    for (const e of entries) {
        const path = join(dir, e.name)
        if (e.isDirectory()) {
            // Skip irrelevant trees
            if (['node_modules', '.svelte-kit', 'dist', 'build', '.git'].includes(e.name)) continue
            yield* walkSource(path)
        } else if (SCAN_EXTS.some((ext) => e.name.endsWith(ext))) {
            yield path
        }
    }
}

async function collectReferences() {
    /** @type {Map<string, Array<{file: string, line: number}>>} */
    const refs = new Map()
    for (const scanDir of SCAN_DIRS) {
        for await (const filePath of walkSource(join(ROOT, scanDir))) {
            const rel = relative(ROOT, filePath)
            if (SELF_FILES.has(rel)) continue
            const text = await readFile(filePath, 'utf8')
            const lines = text.split('\n')
            for (const pattern of REFERENCE_PATTERNS) {
                pattern.lastIndex = 0
                let match
                while ((match = pattern.exec(text)) !== null) {
                    const key = match[1]
                    // Compute line number from match index
                    const upToMatch = text.slice(0, match.index)
                    const lineNo = upToMatch.split('\n').length
                    if (!refs.has(key)) refs.set(key, [])
                    refs.get(key).push({ file: rel, line: lineNo })
                }
            }
            void lines
        }
    }
    return refs
}

// ---------------------------------------------------------------------------
// Main

async function main() {
    const helpEnSrc = await readFile(join(ROOT, HELP_EN), 'utf8')
    const helpKoSrc = await readFile(join(ROOT, HELP_KO), 'utf8')

    const enKeys = extractKeys(helpEnSrc)
    const koKeys = extractKeys(helpKoSrc)
    const refs = await collectReferences()

    // Compute issues
    const missing = [] // referenced in code, not defined in help.en.ts
    const dead = [] // defined in help.en.ts, not referenced anywhere
    const untranslated = [] // defined in en, not in ko

    for (const [key, locations] of refs) {
        if (!enKeys.has(key)) missing.push({ key, locations })
    }

    for (const key of enKeys) {
        if (!refs.has(key)) dead.push(key)
    }

    for (const key of enKeys) {
        if (!koKeys.has(key)) untranslated.push(key)
    }

    // Output
    if (asJson) {
        const out = {
            counts: {
                enKeys: enKeys.size,
                koKeys: koKeys.size,
                referenced: refs.size,
                missing: missing.length,
                dead: dead.length,
                untranslated: untranslated.length,
            },
            missing,
            dead: dead.sort(),
            untranslated: untranslated.sort(),
        }
        console.log(JSON.stringify(out, null, 2))
    } else {
        console.log('=== Help Key Audit ===')
        console.log(`  help.en.ts keys:     ${enKeys.size}`)
        console.log(`  help.ko.ts keys:     ${koKeys.size}`)
        console.log(`  referenced in code:  ${refs.size}`)
        console.log()

        if (missing.length === 0) {
            console.log('✓ No missing keys (every <Help key=>/helpKey: reference is defined).')
        } else {
            console.log(`✗ Missing keys (${missing.length}) — referenced in code but not defined in help.en.ts:`)
            for (const { key, locations } of missing.sort((a, b) => a.key.localeCompare(b.key))) {
                console.log(`    "${key}"`)
                for (const loc of locations.slice(0, 3)) {
                    console.log(`      ${loc.file}:${loc.line}`)
                }
                if (locations.length > 3) console.log(`      ... +${locations.length - 3} more`)
            }
        }
        console.log()

        if (dead.length === 0) {
            console.log('✓ No dead keys.')
        } else {
            console.log(`⚠ Dead keys (${dead.length}) — defined in help.en.ts, not referenced anywhere:`)
            for (const k of dead.sort()) console.log(`    "${k}"`)
        }
        console.log()

        if (untranslated.length === 0) {
            console.log('✓ Korean translation is complete.')
        } else {
            console.log(`⚠ Untranslated keys (${untranslated.length}) — present in help.en.ts, missing in help.ko.ts:`)
            for (const k of untranslated.sort()) console.log(`    "${k}"`)
        }
    }

    // Exit code
    const hasIssues = missing.length > 0 || (strict && (dead.length > 0 || untranslated.length > 0))
    process.exit(hasIssues ? 1 : 0)
}

main().catch((err) => {
    console.error('check-help-keys failed:', err)
    process.exit(2)
})
